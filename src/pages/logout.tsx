import Layout from '@/components/templates/Layout';
import { APP_NAME, LOCAL_USER_ID, LOCAL_USER_TYPE } from '@/constant';
import { localCookieClearToken } from '@/lib/Cookies/token';
import { setLocal } from '@/lib/LocalStorage/localStorage';
import type { NextPage } from 'next';
import Head from 'next/head';
import Router from 'next/router';
import { useEffect } from 'react';

// interface IReduxStateWorkspace {
//   user?: IUser;
// }

const LogoutAdmin: NextPage = () => {
  useEffect(() => {
    setLocal(LOCAL_USER_TYPE, '');
    setLocal(LOCAL_USER_ID, '');
    localCookieClearToken();
    Router.replace('/login');
  });

  return (
    <Layout>
      <Head>
        <title>{APP_NAME} | Logout</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>
    </Layout>
  );
};

export default LogoutAdmin;
